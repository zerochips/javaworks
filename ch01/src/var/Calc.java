package var;

public class Calc {

	public static void main(String[] args) {
		// 두 수의 합, 차, 곱, 나누기
//		int x = 10;
//		int y = 20;
		int x = 10, y =20;
		
		System.out.println(x + y);
		System.out.println(x - y);
		System.out.println(x * y);
		// 정수 연산 : 결과 - 정수로 출력 -> 실수 연산(자료형을 실수혀으로 바꿔야함)
		// 실수형으로 바꿔야되기 때문에 double형으로 형 변환
		System.out.println((double)x / y);
	}

}
